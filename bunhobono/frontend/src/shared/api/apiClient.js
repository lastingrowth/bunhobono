// axios 설정 공용화 파일

import axios from "axios";

/*
|--------------------------------------------------------------------------
| 1. Axios 인스턴스 생성 (핵심)
|--------------------------------------------------------------------------
| axios를 그대로 쓰지 않고 "인스턴스"를 만들어서 사용한다.
| 이유:
| - baseURL 한 곳에서 관리
| - 인터셉터 공통 적용
| - 설정 재사용 가능
*/
const api = axios.create({

    /*
    |--------------------------------------------------------------------------
    | baseURL
    |--------------------------------------------------------------------------
    | 모든 API 요청의 기본 주소
    |
    | 예)
    | VITE_API_URL = http://localhost/api
    |
    | api.get("/users")
    | → http://localhost/api/users
    |
    | ⚠️ 반드시 .env에 있어야 함
    */
    baseURL: import.meta.env.VITE_API_URL,

    /*
    |--------------------------------------------------------------------------
    | timeout
    |--------------------------------------------------------------------------
    | 서버가 응답 안 할 때 무한 대기 방지
    | 10초(10000ms) 지나면 자동 에러 발생
    */
    timeout: 10000,

    /*
    |--------------------------------------------------------------------------
    | headers (기본 헤더)
    |--------------------------------------------------------------------------
    | 모든 요청에 기본으로 붙는 헤더
    */
    headers: {
        "Content-Type": "application/json",
    }
});


/*
|--------------------------------------------------------------------------
| 2. Request Interceptor (요청 보내기 직전)
|--------------------------------------------------------------------------
| API 요청 직전에 실행됨
|
| 주요 역할:
| - JWT 토큰 자동 삽입
| - 로그 출력
| - 공통 헤더 추가
*/
api.interceptors.request.use(

    (config) => {

        /*
        --------------------------------------------------------
        JWT 토큰 자동 추가 (로그인 기능 만들면 필수)
        --------------------------------------------------------
        */
        const token = localStorage.getItem("token");

        if (token) {
            config.headers.Authorization = `Bearer ${token}`;
        }

        // 개발용 로그 (나중에 삭제 가능)
        console.log(`[API REQUEST] ${config.method?.toUpperCase()} ${config.url}`);

        return config;
    },

    (error) => {
        // 요청 자체가 실패한 경우
        return Promise.reject(error);
    }
);


/*
|--------------------------------------------------------------------------
| 3. Response Interceptor (응답 받자마자)
|--------------------------------------------------------------------------
| 모든 API 응답을 공통 처리하는 곳
|
| 장점:
| - 에러 처리 중앙화
| - 로그인 만료 처리 가능
| - 공통 메시지 처리 가능
*/
api.interceptors.response.use(

    (response) => {
        // 성공 응답 (200 ~ 299)

        /*
        axios는 response 전체를 주기 때문에
        실제 데이터만 쓰고 싶으면 response.data 사용
        */

        return response;
    },

    (error) => {

        /*
        --------------------------------------------------------
        서버가 응답을 준 경우 (HTTP 에러)
        --------------------------------------------------------
        */
        if (error.response) {

            const status = error.response.status;

            if (status === 401) {
                console.error("🚨 인증 실패 (로그인 필요)");
            }

            if (status === 403) {
                console.error("🚨 권한 없음");
            }

            if (status === 404) {
                console.error("🚨 API 없음");
            }

            if (status === 500) {
                console.error("🚨 서버 오류");
            }
        }

        /*
        --------------------------------------------------------
        서버 자체가 응답 안 준 경우 (네트워크 문제)
        --------------------------------------------------------
        */
        else if (error.request) {
            console.error("🚨 서버 응답 없음 (네트워크 문제)");
        }

        /*
        --------------------------------------------------------
        axios 설정 오류
        --------------------------------------------------------
        */
        else {
            console.error("🚨 Axios 설정 오류:", error.message);
        }

        return Promise.reject(error);
    }
);


/*
|--------------------------------------------------------------------------
| 4. export
|--------------------------------------------------------------------------
| 이걸 다른 파일에서 import 해서 사용
*/
export default api;

/*
|--------------------------------------------------------------------------
|   사용 예시
|--------------------------------------------------------------------------
| import api from "@/shared/api/apiClient";
|
| const res = await api.get("/users");
| console.log(res.data);

*/